package w.asm.patcher;

import lombok.AccessLevel;
import lombok.Getter;
import lombok.RequiredArgsConstructor;
import lombok.experimental.FieldDefaults;
import lombok.val;
import org.jetbrains.annotations.NotNull;
import org.objectweb.asm.Type;

/**
 * @author whilein
 */
@Getter
@FieldDefaults(level = AccessLevel.PRIVATE, makeFinal = true)
@RequiredArgsConstructor(access = AccessLevel.PRIVATE)
public final class ImmutableMethodInfo implements MethodInfo {

    Type returnType;
    Type[] arguments;

    String name;

    public static @NotNull MethodInfo from(final @NotNull String name, final @NotNull String descriptor) {
        val type = Type.getMethodType(descriptor);

        return new ImmutableMethodInfo(
                type.getReturnType(), type.getArgumentTypes(),
                name
        );
    }
}
